_CODE_BUILDERS = {}

def register_code_builder(target: Target):
    def decorator(fn):
        _CODE_BUILDERS[target] = fn
        return fn
    return decorator

class RelationshipCanvas:
    def __init__(self, datasets: Dict[str, pd.DataFrame]):
        self.datasets = {k: v.copy(deep=False) for k, v in datasets.items()}
        self.links: List[Tuple] = []
        self.plan: List[Dict[str, Any]] = []
        self.start_time = None
        
        # FIXED: Persistent SQL naming state for idempotent generation
        self._sql_used_names = set()
        self._sql_step_map = {}
        self._last_join_result = None

    def _say(self, level: LogLevel, message: str, **meta):
        """Add a structured step to the execution plan"""
        step = {
            "level": level,
            "message": message,
            "timestamp": time.time(),
            "meta": meta
        }
        self.plan.append(step)
        
        if (hasattr(st, '_is_running_with_streamlit') and 
            st._is_running_with_streamlit and
            level in [LogLevel.START, LogLevel.STEP, LogLevel.ERROR]):
            st.caption(f"▸ {message}")

    def clear_plan(self):
        """Clear the execution plan"""
        self.plan = []
        # FIXED: Clear SQL naming state when plan changes
        self._sql_used_names.clear()
        self._sql_step_map.clear()

    def _safe_union_select(self, table: str, all_columns: List[str]) -> str:
        """Generate safe SELECT clause with NULL padding for missing columns"""
        df = self.datasets[table]
        selects = []
        for col in all_columns:
            if col in df.columns:
                selects.append(f'"{col}"')
            else:
                selects.append(f"NULL AS \"{col}\"")
        return ", ".join(selects)

    def to_sql(self) -> str:
        """Generate clean, executable SQL with safe UNION handling and COALESCE for PKs"""
        if not self.plan:
            return "-- No operations executed\n-- Build relationships first, then generate SQL"
        
        sql_lines = [
            "-- SQL generated by Table Relationship Engine", 
            "-- ANSI SQL - works in Snowflake, BigQuery, Postgres, Redshift",
            "-- Safe UNION handling with COALESCE for primary keys",
            ""
        ]
        
        ctes = []
        table_map = {}

        def get_short_name(left: str, right: str, op: str) -> str:
            """Generate short, unique CTE names (max 40 chars) with idempotency"""
            abbr_map = {"customers": "cust", "orders": "ord", "products": "prod", 
                       "payments": "pay", "union": "u", "intersect": "i", "except": "x",
                       "jinner": "j", "jleft": "jl", "jright": "jr", "jouter": "jo"}
            
            left_short = abbr_map.get(left, left[:8])
            right_short = abbr_map.get(right, right[:8])
            op_short = abbr_map.get(op, op[:4])
            
            base_name = f"{left_short}_{op_short}_{right_short}"
            
            if base_name not in self._sql_used_names:
                self._sql_used_names.add(base_name)
                return base_name
                
            for i in range(2, 10):
                candidate = f"{base_name}_{i}"
                if candidate not in self._sql_used_names:
                    self._sql_used_names.add(candidate)
                    return candidate
                    
            return base_name

        for i, step in enumerate(self.plan):
            level = step["level"]
            msg = step["message"]
            meta = step.get("meta", {})

            if level == LogLevel.STEP:
                if "Set:" in msg:
                    parts = msg.split()
                    table1, operation, table2 = parts[1], parts[2], parts[3]
                    
                    # FIXED: Use step map for idempotent naming across sessions
                    step_key = f"set_{table1}_{operation}_{table2}"
                    if step_key not in self._sql_step_map:
                        self._sql_step_map[step_key] = get_short_name(table1, table2, operation)
                    
                    cte_name = self._sql_step_map[step_key]
                    t1_ref = table_map.get(table1, table1)
                    t2_ref = table_map.get(table2, table2)
                    
                    # FIXED: Safe UNION handling with COALESCE for primary keys
                    if operation in ["union", "union_all"]:
                        all_columns = sorted(set(self.datasets[table1].columns) | set(self.datasets[table2].columns))
                        select1 = self._safe_union_select(table1, all_columns)
                        select2 = self._safe_union_select(table2, all_columns)
                        
                        union_op = "UNION ALL" if operation == "union_all" else "UNION"
                        comment = f"-- Safe {union_op} of {table1} and {table2} with column alignment"
                        cte_sql = f"{comment}\n{cte_name} AS (\n  SELECT {select1} FROM {t1_ref}\n  {union_op}\n  SELECT {select2} FROM {t2_ref}\n)"
                    elif operation == "intersect":
                        common_cols = sorted(set(self.datasets[table1].columns) & set(self.datasets[table2].columns))
                        if common_cols:
                            cols_str = ", ".join(f'"{c}"' for c in common_cols)
                            comment = f"-- INTERSECT of {table1} and {table2}"
                            cte_sql = f"{comment}\n{cte_name} AS (\n  SELECT {cols_str} FROM {t1_ref}\n  INTERSECT\n  SELECT {cols_str} FROM {t2_ref}\n)"
                        else:
                            comment = f"-- Empty INTERSECT (no common columns)"
                            cte_sql = f"{comment}\n{cte_name} AS (SELECT 1 as no_common_columns WHERE FALSE)"
                    elif operation == "except":
                        comment = f"-- EXCEPT: {table1} - {table2}"
                        cte_sql = f"{comment}\n{cte_name} AS (\n  SELECT * FROM {t1_ref}\n  EXCEPT\n  SELECT * FROM {t2_ref}\n)"
                    else:
                        comment = f"-- Set operation: {table1} {operation} {table2}"
                        cte_sql = f"{comment}\n{cte_name} AS (SELECT * FROM {t1_ref})"  # Fallback
                    
                    ctes.append(cte_sql)
                    table_map[table1] = cte_name
                    table_map[table2] = cte_name
                    
                elif "Join:" in msg:
                    parts = msg.split()
                    left_table, right_table = parts[1], parts[3]
                    join_type = parts[4][1:-1].lower()
                    
                    # FIXED: Use step map for idempotent naming across sessions
                    step_key = f"join_{left_table}_{right_table}_{join_type}"
                    if step_key not in self._sql_step_map:
                        self._sql_step_map[step_key] = get_short_name(left_table, right_table, f"j{join_type[:1]}")
                    
                    cte_name = self._sql_step_map[step_key]
                    left_ref = table_map.get(left_table, left_table)
                    right_ref = table_map.get(right_table, right_table)
                    on_clause = meta.get("on", f"{left_table}_id = {right_table}_id")
                    
                    comment = f"-- {join_type.upper()} JOIN {left_table} with {right_table}"
                    cte_sql = f"{comment}\n{cte_name} AS (\n  SELECT *\n  FROM {left_ref}\n  {join_type.upper()} JOIN {right_ref}\n    ON {on_clause}\n)"
                    ctes.append(cte_sql)
                    
                    table_map[left_table] = cte_name
                    table_map[right_table] = cte_name

            elif level == LogLevel.START:
                start_table = msg.split("'")[1]
                table_map[start_table] = start_table

        if ctes:
            sql_lines.append("WITH")
            sql_lines.append(",\n".join(ctes))
            final_table = list(table_map.values())[-1] if table_map else "your_table"
            sql_lines.append(f"SELECT * FROM {final_table};")
        else:
            start_ref = next(iter(table_map.values()), "your_table")
            sql_lines.append(f"SELECT * FROM {start_ref};")

        return "\n".join(sql_lines)

