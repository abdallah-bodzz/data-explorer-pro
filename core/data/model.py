        errors = len([p for p in self.plan if p["level"] == LogLevel.ERROR])
        warnings = len([p for p in self.plan if p["level"] == LogLevel.WARN])
        
        return {
            "joins": joins,
            "set_ops": set_ops, 
            "errors": errors,
            "warnings": warnings,
            "total_operations": joins + set_ops
        }